#include "opentelemetry/trace/trace_state.h"

#include <gtest/gtest.h>

namespace
{

using opentelemetry::trace::TraceState;

TEST(TraceStateTest, DefaultConstruction)
{
  TraceState s;
  EXPECT_FALSE(s.Get("missing_key", "value"));
  EXPECT_TRUE(s.Empty());
  EXPECT_EQ(0, s.Entries().size());
}
}  // namespace
